package com.attijari.config.optConfig;

public class LeverOpposCompte {
    private String age;
    private String ncp;
    private String motifOppos;


    // Getter Methods

    public String getAge() {
        return age;
    }

    public String getNcp() {
        return ncp;
    }

    public String getMotifOppos() {
        return motifOppos;
    }

    // Setter Methods

    public void setAge(String age) {
        this.age = age;
    }

    public void setNcp(String ncp) {
        this.ncp = ncp;
    }

    public void setMotifOppos(String motifOppos) {
        this.motifOppos = motifOppos;
    }
}
